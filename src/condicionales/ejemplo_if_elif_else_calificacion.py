nota = 87
if nota >= 90:
    # Rango de sobresaliente
    print("Calificación: Sobresaliente")
elif nota >= 80:
    # Rango de notable (nota entre 80 y 89)
    print("Calificación: Notable")
elif nota >= 70:
    # Rango de aprobado (nota entre 70 y 79)
    print("Calificación: Aprobado")
else:
    # Nota menor de 70: suspenso
    print("Calificación: Suspenso")